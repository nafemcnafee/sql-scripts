/* NOTE: Unsold cars DO NOT have options */ 
SELECT serial, make, model
  FROM s11.car 
 WHERE serial IN (SELECT serial FROM s11.car WHERE cyear = '2016' 
                  MINUS 
                  SELECT serial FROM s11.saleinv)
 ORDER BY serial ASC;
